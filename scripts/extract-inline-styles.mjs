// One-shot: extract every static inline style="…" in the templates into a
// generated stylesheet of classes (identical declarations → zero visual
// change), so we can drop style-src 'unsafe-inline' (issue #5 → #4).
// Each style string is a quote-delimited literal (no substring collisions);
// `class` always precedes `style`, so a class-merge regex is safe.

import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";

const DIRS = ["src/pages", "src/components", "src/layouts"];
const files = [];
(function walk(dirs) {
  for (const d of dirs) for (const f of fs.readdirSync(d)) {
    const p = path.join(d, f);
    if (fs.statSync(p).isDirectory()) walk([p]);
    else if (p.endsWith(".astro")) files.push(p);
  }
})(DIRS);

const nameFor = (s) => "u" + crypto.createHash("sha1").update(s).digest("hex").slice(0, 7);
const escapeRe = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");

// collect unique style strings
const rules = new Map(); // string -> class
for (const file of files) {
  const html = fs.readFileSync(file, "utf-8");
  for (const m of html.matchAll(/style="([^"]*)"/g)) {
    const s = m[1];
    if (!rules.has(s)) rules.set(s, nameFor(s));
  }
}

// special case: the one class:list element (index.astro) — inject the class
// into the array rather than adding a second class attribute
for (const file of files) {
  let html = fs.readFileSync(file, "utf-8");
  html = html.replace(
    /class:list=\{(\[[^\]]*\])\}([^>]*?)style="([^"]*)"/g,
    (_, arr, mid, s) => `class:list={[...${arr}, "${rules.get(s)}"]}${mid}`,
  );
  fs.writeFileSync(file, html);
}

// merge into existing class="…", then convert standalone style="…"
let merged = 0, standalone = 0;
for (const file of files) {
  let html = fs.readFileSync(file, "utf-8");
  for (const [s, cls] of rules) {
    const before = html;
    html = html.replace(new RegExp(`class="([^"]*)"([^>]*?)style="${escapeRe(s)}"`, "g"), (_, c, mid) => {
      merged++; return `class="${c} ${cls}"${mid}`;
    });
    if (html !== before) { /* merged some */ }
    const parts = html.split(`style="${s}"`);
    if (parts.length > 1) { standalone += parts.length - 1; html = parts.join(`class="${cls}"`); }
  }
  fs.writeFileSync(file, html);
}

// emit stylesheet (sorted for stable diffs)
const css =
  "/* GENERATED by scripts/extract-inline-styles.mjs — extracted from template\n" +
  "   inline styles so the CSP can drop style-src 'unsafe-inline'. Edit the\n" +
  "   source templates' classes, not this file. */\n\n" +
  [...rules.entries()].sort((a, b) => a[1].localeCompare(b[1]))
    .map(([s, cls]) => `.${cls} { ${s.replace(/;\s*$/, "")}; }`).join("\n") + "\n";
fs.writeFileSync("src/styles/extracted.css", css);

console.log(`✓ ${rules.size} classes · ${merged} merged into class · ${standalone} standalone → src/styles/extracted.css`);
